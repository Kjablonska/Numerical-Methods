function r = RK4_helper(x, h)

x1 = @(x) (x(2) + x(1) * (0.5 - x(1)^2 - x(2)^2));
x2 = @(x) (-x(1) + x(2) * (0.5 - x(1)^2 - x(2)^2));


k11 = x1(x);
k12 = x2(x);

k21 = x1([x(1) + 1/2*h*k11,x(2) + 1/2*h*k12]);
k22 = x2([x(1) + 1/2*h*k11,x(2) + 1/2*h*k12]);

k31 = x1([x(1) + 1/2*h*k21,x(2) + 1/2*h*k22]);
k32 = x2([x(1) + 1/2*h*k21,x(2) + 1/2*h*k22]);

k41 = x1([x(1) + h*k31,x(2) + h*k32]);
k42 = x2([x(1) + h*k31,x(2) + h*k32]);

r(1) = x(1) + (1/6*h*(k11 + 2*k21 + 2*k31 + k41));
r(2) = x(2) + (1/6*h*(k12 + 2*k22 + 2*k32 + k42));

end

